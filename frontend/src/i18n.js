export const SUPPORTED_LANGUAGES = ['en', 'cs']

export function normalizeLanguage(value) {
  const normalized = String(value ?? '').trim().toLowerCase()
  return SUPPORTED_LANGUAGES.includes(normalized) ? normalized : 'en'
}

